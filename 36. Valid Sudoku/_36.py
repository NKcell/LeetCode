class Solution:
    def isValidSudoku(self, board) -> bool:
        res = list()
        for i,row in enumerate(board):
            for j, v in enumerate(row):
                if v != '.':
                    res += [(i,v), (v, j), (i//3, j//3, v)]

        print(len(res), len(set(res)))
        return len(res) == len(set(res))

